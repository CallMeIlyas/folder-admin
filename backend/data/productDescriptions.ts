// Helper function untuk mendapatkan deskripsi produk
export const getProductDescription = (category: string, productName: string) => {
  // Normalize category name
  const normalizedCategory = category?.trim() || "";
  
  // Find category in our data
  const categoryData = ENGLISH_VERSION_PRODUCT_DETAILS[normalizedCategory as keyof typeof ENGLISH_VERSION_PRODUCT_DETAILS];
  
  if (!categoryData) {
    // Try to find by partial match for category
    const categoryKey = Object.keys(ENGLISH_VERSION_PRODUCT_DETAILS).find(key => 
      normalizedCategory.toLowerCase().includes(key.toLowerCase()) ||
      key.toLowerCase().includes(normalizedCategory.toLowerCase())
    );
    
    if (!categoryKey) return null;
    
    const foundCategoryData = ENGLISH_VERSION_PRODUCT_DETAILS[categoryKey as keyof typeof ENGLISH_VERSION_PRODUCT_DETAILS];
    
    // Now find product in this category
    const productKey = Object.keys(foundCategoryData).find(key => {
      const normalizedProductName = productName?.toLowerCase() || "";
      const normalizedKey = key.toLowerCase();
      
      return normalizedProductName.includes(normalizedKey) ||
             normalizedKey.includes(normalizedProductName) ||
             productName?.toLowerCase().includes(key.toLowerCase()) ||
             key.toLowerCase().includes(productName?.toLowerCase() || "");
    });
    
    return productKey ? foundCategoryData[productKey as keyof typeof foundCategoryData] : null;
  }
  
  // Cari produk dalam kategori yang ditemukan
  const normalizedProductName = productName?.toLowerCase() || "";
  
  const productKey = Object.keys(categoryData).find(key => {
    const normalizedKey = key.toLowerCase();
    
    // Exact match or partial match
    if (normalizedProductName === normalizedKey) return true;
    if (normalizedProductName.includes(normalizedKey)) return true;
    if (normalizedKey.includes(normalizedProductName)) return true;
    
    // Special handling for sizes like A0-80X110CM
    if (normalizedProductName.includes("a0") && normalizedKey.includes("a0/80x110cm")) return true;
    if (normalizedProductName.includes("a1") && normalizedKey.includes("a1/55x80cm")) return true;
    if (normalizedProductName.includes("a2") && normalizedKey.includes("a2/40x55cm")) return true;
    
    // Handle R sizes
    if (normalizedProductName.includes("12r") && normalizedKey.includes("12r")) return true;
    if (normalizedProductName.includes("10r") && normalizedKey.includes("10r")) return true;
    if (normalizedProductName.includes("8r") && normalizedKey.includes("8r")) return true;
    if (normalizedProductName.includes("6r") && normalizedKey.includes("6r")) return true;
    if (normalizedProductName.includes("4r") && normalizedKey.includes("4r")) return true;
    
    // Handle square sizes
    if (normalizedProductName.includes("20x20") && normalizedKey.includes("20x20cm")) return true;
    if (normalizedProductName.includes("15x15") && normalizedKey.includes("15x15cm")) return true;
    
    return false;
  });
  
  return productKey ? categoryData[productKey as keyof typeof categoryData] : null;
};

// Helper untuk mendapatkan title produk
export const getProductTitle = (category: string, productName: string): string => {
  const description = getProductDescription(category, productName);
  return description?.title || `${category} ${productName}`;
};